	<p>Arun over the years, has carved a niche for himself in the world of percussion as one of the few drummers who does inter-disciplinary music. He has trained for jazz drumming under his father Sri B.S Sukumar and the legendary drummer Sri Ranjit Barot. Arun is also trained in Carnatic percussions under Vidwan S.V.Giridhar and Vidwan Anoor R Anantha Krishna Sharma. He was initiated to Carnatic music from the Legendary Vocalist 'Karnataka kala tilaka' Vidwan Anoor Suryanarayana.</p>
	<p>For the past 20 years, Arun has been sharing the stage regularly with Indian classical music’s geniuses like Sangita Kalanidhi’s  Dr. R K Srikantan, Dr. M Balamuralikrishna,  A K C Natarajan, Dr. T V Gopalakrishnan, Sudha Raghunathan, Dr. L Subramaniam, M S Sheela, Nithyashree Mahadevan, Vijay Siva, Abhishek Raghuram, Pattabhirama Pandit, Sikki Gurucharan, Dr. Kadri Gopalnath, Chitraveena N Ravikiran, Ganesh Kumaresh, Nagraj &amp; Dr Manjunath, Dr. Jayanthi Kumaresh, Dr. Suma Sudhindra, Lalgudi G J R Krishnan &amp; Lalgudi VijayaLakshmi, Ronu Muzumdar, Rakesh Chaurasia, U Rajesh, Rajesh Vaidya, Purbayun Chaterjee, Stephan Devassy and many more.</p>
	<p>He has performed regularly with the Legendary playback singers like Dr P B Srinivas, Dr Rajkumar, Dr S P Balasubramaniam, Dr K J Yesudas, S Janaki, Vani Jayaram, P Sushela, K S Chitra,Kavitha Krishnamurthy, Malgudi Shubha, Hariharan, Shankar Mahadevan, Vijay Prakash, Karthick and many alike. He also is an active member of the music fraternity in the South Indian Film Industry.</p>
	<p>Have been featuring regularly with various percussion legends in the field of Mridangam, Tabla and Tavil such as Sangita Kalacharya Vikku Vinayakram, Ustad Zakir Hussain, Sangita Kalanidhi Trichy Sankaran, Bickram Ghosh, Vijay Ghate, Selva Ganesh, Yogesh Shamsi, Mannargudi Vasudevan, Muttukumara Swamy, Fazal Qureshi, Mannargudi Eashwaran, K V Prasad, Anoor Anantha Krishna Sharma, Arjun Kumar and many alike.</p>
	<p>Arun is B-High graded artist in the All India Radio for versatile Morching playing. Arun is one of the founder members of Percussion ensemble group called “Layatharanga”.</p>
	<p>Arun has also been very active in the recording industry. Some of his recording projects are-</p>
